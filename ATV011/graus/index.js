let grausCelsius = Number(prompt("Digite a temperatura em graus Celsius: "));
let grausFahrenheit = (grausCelsius * 9 / 5) + 32;
console.log(`A temperatura de ${grausCelsius} graus Celsius é equivalente a ${grausFahrenheit.toFixed(2)} graus Fahrenheit`);
